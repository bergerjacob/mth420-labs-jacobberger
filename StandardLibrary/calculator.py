from math import sqrt

def sum(x, y):
    """
    Calculates the sum of two numbers.

    Parameters:
        x: The first number.
        y: The second number.
    Returns:
        The sum of x and y.
    """
    return x + y

def product(x, y):
    """
    Calculates the product of two numbers.

    Parameters:
        x: The first number.
        y: The second number.
    Returns:
        The product of x and y.
    """
    return x * y
